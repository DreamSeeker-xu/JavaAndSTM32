void SENDTIM_IRQ_FUN(void)
{
		if(TIM_GetITStatus(SENDTIM_X,TIM_IT_Update) != RESET)
		{
				//1s时间到
				time_1s = 1;
				//清除定时器中断标志位
				TIM_ClearITPendingBit(SENDTIM_X,TIM_FLAG_Update);	
		}
}
void DEBUG_USART2_IRQHandler(void)
{
	  uint8_t temp = 0;
		if(USART_GetITStatus(DEBUG_USART2x,USART_IT_RXNE) != RESET)//接收到数据并存入USART_DR寄存器,等待读取
		{ 
				temp = USART_ReceiveData(DEBUG_USART2x);//读取接收到的(8位数据)
				if(Esp8266_Receive.Receive_Length < RECEIVE_MAX_Length - 1)//保留最后一位写结束符
				{
						Esp8266_Receive.Receive_Data[Esp8266_Receive.Receive_Length ++] = temp;//读取的字节放入数组
				}
		}
		if ( USART_GetITStatus( DEBUG_USART2x, USART_IT_IDLE ) == SET)//数据帧接收完毕,总线空闲
		{
				Esp8266_Receive.Receive_FinshFlag = 1;
				temp = USART_ReceiveData(DEBUG_USART2x);//读取一次，清除空闲中断 	
		}	
}



